package com.suraj.weatherinfo.pojo;

public class WeatherInfo {
	private String cityName;
	private String date;
	private double dayTemp;
	private double nightTemp;
	private double eveningTemp;
	private double morningTemp;
	private String weatherStatus;
	private double pressure;
	private int humidity;
	private double windSpeed;
	private String iconName;
	private double lattitude;
	private double longitude;
	private String weatherStatusDescription;
	private String weatherImagePath;

	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getWeatherStatus() {
		return weatherStatus;
	}
	public void setWeatherStatus(String weatherStatus) {
		this.weatherStatus = weatherStatus;
	}
	public double getPressure() {
		return pressure;
	}
	public void setPressure(double pressure) {
		this.pressure = pressure;
	}
	public int getHumidity() {
		return humidity;
	}
	public void setHumidity(int humidity) {
		this.humidity = humidity;
	}
	public double getWindSpeed() {
		return windSpeed;
	}
	public void setWindSpeed(double windSpeed) {
		this.windSpeed = windSpeed;
	}
	public String getIconName() {
		return iconName;
	}
	public void setIconName(String iconName) {
		this.iconName = iconName;
	}
	public double getNightTemp() {
		return nightTemp;
	}
	public void setNightTemp(double nightTemp) {
		this.nightTemp = nightTemp;
	}
	public double getEveningTemp() {
		return eveningTemp;
	}
	public void setEveningTemp(double eveningTemp) {
		this.eveningTemp = eveningTemp;
	}
	public double getMorningTemp() {
		return morningTemp;
	}
	public void setMorningTemp(double morningTemp) {
		this.morningTemp = morningTemp;
	}
	public double getLattitude() {
		return lattitude;
	}
	public void setLattitude(double lattitude) {
		this.lattitude = lattitude;
	}
	public double getLongitude() {
		return longitude;
	}
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	public double getDayTemp() {
		return dayTemp;
	}
	public void setDayTemp(double dayTemp) {
		this.dayTemp = dayTemp;
	}
	public String getWeatherStatusDescription() {
		return weatherStatusDescription;
	}
	public void setWeatherStatusDescription(String weatherStatusDescription) {
		this.weatherStatusDescription = weatherStatusDescription;
	}
	public String getWeatherImagePath() {
		return weatherImagePath;
	}
	public void setWeatherImagePath(String weatherImagePath) {
		this.weatherImagePath = weatherImagePath;
	}
	@Override
	public String toString() {
		return "WeatherInfo [cityName=" + cityName + ", date=" + date
				+ ", dayTemp=" + dayTemp + ", nightTemp=" + nightTemp
				+ ", eveningTemp=" + eveningTemp + ", morningTemp="
				+ morningTemp + ", weatherStatus=" + weatherStatus
				+ ", pressure=" + pressure + ", humidity=" + humidity
				+ ", windSpeed=" + windSpeed + ", iconName=" + iconName
				+ ", lattitude=" + lattitude + ", longitude=" + longitude
				+ ", weatherStatusDescription=" + weatherStatusDescription
				+ ", weatherImagePath=" + weatherImagePath + "]";
	}
}
